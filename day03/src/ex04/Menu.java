package ex04;

public class Menu {
	String name;
	int price;
	
	// 기본생성자는 자동으로 작성된다 (생성자 코드를 직접 작성하지 않은 경우에만)
		
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
		
}
